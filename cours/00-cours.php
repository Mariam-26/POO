<h2 class="text-center">Les avantages de la POO </h2>


<P>Le principal avantage de POO est de faciliter la réutilisation d'une module.Ainsi, on pourra reprendre les méthodes (fonctions) d'une classe existante dans une nouvelle classe grâce à la notion d'géritage.  </P>
<p>Une autre chose facilitée par l'orienté objet est la lecture du code.En effet on comprendra plus facilement une instruction <code>->ajout</code>, qu'un if elese avec des multiples conditions dans une boucle. La force de cette façon de programmer est qu'elle se calque sur la réalité physique.Une classe sera par exemple Renault.Les objets, ou instanciations de cette classe seront $voiture1, $voiture2, etc.L'objet donc la voiture. Cet objet aura des caractéristiques représentées  par des variables, appelées propriétés, et des comportements représentées par des fonctions, appelées méthodes. En conclusion, les avantages de cette façon de programmer sont:</p>



<ul>   
    <li>La facilitation du travail collobaratif</li>

    <li>La simplification de la maintenance</li>

    <li>L'assouplissement du code</li>

</ul>

<p>Cette approche est toute de même moins intuitive que l'approche procédurale. La POO oblige en effet à réfléchir et modéliser avant de programmer</p>

